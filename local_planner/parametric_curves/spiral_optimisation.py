def abcds_f(p: (float, float, float, float, float)):
    a = p[0]
    b = -(11.0 * p[0] / 2.0 - 9.0 * p[1] + 9.0 * p[2] / 2.0 - p[3]) / p[4]
    c = (9.0 * p[0] - 45.0 * p[1] / 2.0 + 18.0 * p[2] - 9.0 * p[3] / 2.0) / p[4] ** 2
    d = (
        -(9.0 * p[0] / 2.0 - 27.0 * p[1] / 2.0 + 27.0 * p[2] / 2.0 - 9.0 * p[3] / 2.0)
        / p[4] ** 3
    )
    arclength = p[4]
    return a, b, c, d, arclength


def K(
    a: np.ndarray, b: np.ndarray, c: np.ndarray, d: np.ndarray, s_values: np.ndarray
) -> np.ndarray:
    return d * s_values**3 + c * s_values**2 + b * s_values + a


def Psi(
    a: np.ndarray,
    b: np.ndarray,
    c: np.ndarray,
    d: np.ndarray,
    s_values: np.ndarray,
    psi_0: np.ndarray,
) -> np.ndarray:
    # all inputs should have the same shape or be scalars
    return (
        d * s_values**4 / 4
        + c * s_values**3 / 3
        + b * s_values**2 / 2
        + a * s_values
        + psi_0
    )


def create_spiral_interpolation(
    x_0, y_0, psi_0, k_0, x_f, y_f, psi_f, k_f, k_max, ds=None, equal=False
) -> (np.ndarray, pd.DataFrame):
    p = optimize_spiral(x_0, y_0, psi_0, k_0, x_f, y_f, psi_f, k_f, k_max, equal)

    if ds is None:
        df = eval_spiral(p, x_0, y_0, psi_0)
    else:
        df = eval_spiral(p, x_0, y_0, psi_0, ds)

    return p, df


def eval_spiral(
    p: (float, float, float, float, float), x_0, y_0, psi_0, ds=0.01
) -> pd.DataFrame:
    a, b, c, d, arclength = abcds_f(p)

    # Calculate the number of points based on the provided step size ds
    point_count = int(np.ceil(arclength / ds))
    s_values = np.linspace(0, arclength, point_count)
    curvature_values = K(a, b, c, d, s_values)
    psi_values = Psi(a, b, c, d, s_values, psi_0)
    x_values = scipy.integrate.cumulative_trapezoid(np.cos(psi_values), s_values, initial=0)  + x_0
    y_values = scipy.integrate.cumulative_trapezoid(np.sin(psi_values), s_values, initial=0) + y_0

    return pd.DataFrame(
        {
            "S": s_values,
            "X": x_values,
            "Y": y_values,
            "Psi": psi_values,
            "K": curvature_values,
        }
    )


def optimize_spiral(
    x_0, y_0, psi_0, k_0, x_f, y_f, psi_f, k_f, k_max: float, equal=False
):
    alpha = 10000.0
    beta = 10000.0
    gamma = 10000.0
    p_0 = k_0
    p_3 = k_f

    arclength_initial_guess = np.hypot(x_f - x_0, y_f - y_0)
    if equal:
        bounds = (
            (k_max, k_max),
            (k_max, k_max),
            (arclength_initial_guess, 3.5 * arclength_initial_guess),
        )
    else:
        bounds = (
            (-k_max, k_max),
            (-k_max, k_max),
            (arclength_initial_guess, 3.5 * arclength_initial_guess),
        )
    params_to_optimize = np.array((0, 0, arclength_initial_guess))

    result = scipy.optimize.minimize(
        objective_function,
        params_to_optimize,
        jac=jac,
        bounds=bounds,
        args=(p_0, p_3, alpha, beta, gamma, x_0, y_0, psi_0, x_f, y_f, psi_f),
        method="L-BFGS-B",
        options={"disp": False},
    )

    p = (p_0, result.x[0], result.x[1], p_3, result.x[2])
    return p


